    public function status( Request $request, $projstatusrowid )
    {
        $status_core = new StatusCore();

        $rec = array();

        $rec[ 'results' ][ 'status' ] = [];

        $rec[ 'results' ][ 'status' ] = $status_core->get( $projstatusrowid );

        $pagevars = array();
        $pagevars[ 'data' ] = array();
        $pagevars[ 'data' ] = $rec;

        return $this->return_success( $request, $pagevars );
    }
    
